    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/studentresultmanagementsystem","root","syed@1234");
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("select * from studentpersonalinfo where Roll_No = '"+Integer.parseInt(txtRollNo.getText())+"'");
            if(rs.next())
            {
                String sql = "update studentpersonalinfo set Status = 0 where Roll_No = '"+Integer.parseInt(txtRollNo.getText())+"'";
                PreparedStatement pstmt = conn.prepareStatement(sql);
                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Deleted Successfully...");
                conn.close();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Roll No does not exist...");
                txtRollNo.setText("");
                txtRollNo.grabFocus();
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

