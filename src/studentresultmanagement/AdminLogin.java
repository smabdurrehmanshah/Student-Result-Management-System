    private boolean validateFields()
    {
        if(txtUsername.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(AdminLogin.this, "Enter Username...");
            txtUsername.grabFocus();
            return false ;
        }
        if(txtPassword.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(AdminLogin.this, "Enter Password...");
            txtPassword.grabFocus();
            return false ;
        }
        return true ;
    }
 

