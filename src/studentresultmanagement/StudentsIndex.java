    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String rollNo = txtRollNo.getText();
        String semesterNo = txtSemesterNo.getText();
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/studentresultmanagementsystem","root","syed@1234");
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("select * from results where Roll_No = '"+Integer.parseInt(txtRollNo.getText())+"' AND Semester_No = '"+Integer.parseInt(txtSemesterNo.getText())+"'");
            if(rs.next())
            {
                this.dispose();
                new StudentResultDetail(rollNo,semesterNo).setVisible(true);      
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Invalid Roll No or Semester No...");
                txtRollNo.setText("");
                txtSemesterNo.setText("");
                txtRollNo.grabFocus();
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
      
    }//GEN-LAST:event_btnSearchActionPerformed

