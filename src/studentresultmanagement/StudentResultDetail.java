    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/studentresultmanagementsystem","root","syed@1234");
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("select srr.Roll_No , spi.Program ,srr.Semester_No ,spi.Name ,srr.SGPA from studentsresultrecord srr inner join studentpersonalinfo spi on srr.Program = spi.Roll_No AND srr.Name = spi.Roll_No where srr.Roll_No = '"+rollNo+"' AND Semester_No = '"+semesterNo+"'");
            if(rs.next())
            {
                txtrollNo.setText(rs.getString("Roll_No"));
                txtProgram.setText(rs.getString("Program"));
                txtsemester.setText(rs.getString("Semester_No"));
                txtName.setText(rs.getString("Name"));
                txtSGPA.setText(rs.getString("SGPA"));  
                
                txtrollNo.setEditable(false);
                txtProgram.setEditable(false);
                txtsemester.setEditable(false);
                txtName.setEditable(false);
                txtSGPA.setEditable(false);
                conn.close();
                
            }
            displayTable();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
       
    }//GEN-LAST:event_formComponentShown

