    private void displayTable()
    {
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/studentresultmanagementsystem","root","syed@1234");
            String sql = "select Subject_Name , Total_Marks , Obtained_Marks , GPA from results where Roll_No = '"+rollNo+"' AND Semester_No = '"+semesterNo+"'";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            ResultSet rs = pstmt.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(rs)); 
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage());
        } 
    }

